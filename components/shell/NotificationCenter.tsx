import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu";
import { useStore } from "@/lib/db/store";
import { cn } from "@/lib/utils";

type NotificationType =
  | "mention"
  | "approval"
  | "deadline"
  | "budget"
  | "invoice"
  | "review";

interface Notification {
  id: string;
  type: NotificationType;
  title: string;
  body: string;
  href: string;
  createdAt: string;
  read: boolean;
  // If set, the notification can be acted on inline without leaving the dropdown.
  inlineAction?: {
    primaryLabel: string;
    secondaryLabel: string;
  };
}

const ICON_MAP: Record<
  NotificationType,
  { icon: React.ComponentType<{ className?: string }>; cls: string }
> = {
  mention: { icon: AtSign, cls: "bg-status-progress/15 text-status-progress" },
  approval: {
    icon: CheckCircle2,
    cls: "bg-status-done/15 text-status-done",
  },
  deadline: {
    icon: Clock,
    cls: "bg-status-revisions/15 text-status-revisions",
  },
  budget: {
    icon: AlertTriangle,
    cls: "bg-status-blocked/15 text-status-blocked",
  },
  invoice: { icon: Receipt, cls: "bg-primary/15 text-primary" },
  review: { icon: Eye, cls: "bg-status-review/15 text-status-review" },
};

