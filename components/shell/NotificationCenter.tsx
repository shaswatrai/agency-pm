export function NotificationCenter() {
  const orgSlug = useStore((s) => s.organization.slug);
  const [items, setItems] = useState(() => buildNotifications(orgSlug));
  const [open, setOpen] = useState(false);

  const unread = items.filter((n) => !n.read).length;

  const markAllRead = () => {
    setItems((arr) => arr.map((n) => ({ ...n, read: true })));
  };

  const markRead = (id: string) => {
    setItems((arr) =>
      arr.map((n) => (n.id === id ? { ...n, read: true } : n)),
    );
  };

  const dismissAfterAction = (id: string, message: string) => {
    setItems((arr) => arr.filter((n) => n.id !== id));
    toast.success(message);
  };

  const handlePrimary = (n: Notification) => (e: React.MouseEvent) => {
    e.preventDefault();
    e.stopPropagation();
    if (n.type === "review")
      dismissAfterAction(n.id, "Approved · team has been notified");
    else if (n.type === "approval")
      dismissAfterAction(n.id, "Invoice queued for sending");
    else dismissAfterAction(n.id, "Done");
  };

  const handleSecondary = (n: Notification) => (e: React.MouseEvent) => {
    e.preventDefault();
    e.stopPropagation();
    if (n.type === "review")
      dismissAfterAction(n.id, "Changes requested");
    else dismissAfterAction(n.id, "Snoozed");
  };

  return (
    <DropdownMenu open={open} onOpenChange={setOpen}>
      <DropdownMenuTrigger asChild>
        <Button variant="ghost" size="icon" aria-label="Notifications" className="relative">
          <Bell className="size-4" />
          <AnimatePresence>
            {unread > 0 ? (
              <motion.span
                initial={{ scale: 0 }}
                animate={{ scale: 1 }}
                exit={{ scale: 0 }}
                className="absolute right-1.5 top-1.5 grid size-4 place-items-center rounded-full bg-status-blocked text-[9px] font-semibold text-white ring-2 ring-background"
              >
                {unread}
              </motion.span>
            ) : null}
          </AnimatePresence>
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="w-[380px] p-0">
        <div className="flex items-center justify-between border-b px-4 py-3">
          <div>
            <p className="text-sm font-semibold">Notifications</p>
            <p className="text-[11px] text-muted-foreground">
              {unread > 0
                ? `${unread} unread`
                : "You're all caught up"}
            </p>
          </div>
          {unread > 0 ? (
            <button
              onClick={markAllRead}
              className="text-xs text-primary hover:underline"
            >
              Mark all read
            </button>
          ) : null}
        </div>

        <div className="max-h-[420px] overflow-y-auto scrollbar-thin">
          {items.length === 0 ? (
            <div className="grid place-items-center py-12 text-center">
              <CheckCircle2 className="mb-2 size-8 text-status-done" />
              <p className="text-sm font-medium">All caught up</p>
              <p className="mt-0.5 text-xs text-muted-foreground">
                No new notifications.
              </p>
            </div>
          ) : (
            <AnimatePresence initial={false}>
              {items.map((n, i) => {
                const meta = ICON_MAP[n.type];
                return (
                  <motion.div
                    key={n.id}
                    layout
                    initial={{ opacity: 0, y: 4 }}
                    animate={{ opacity: 1, y: 0 }}
                    exit={{
                      opacity: 0,
                      x: 30,
                      height: 0,
                      transition: { duration: 0.25 },
                    }}
                    transition={{ delay: i * 0.02 }}
                    className={cn(
                      "border-b transition-colors",
                      !n.read && "bg-primary/5",
                    )}
                  >
                    <Link
                      href={n.href}
                      onClick={() => {
                        markRead(n.id);
                        setOpen(false);
                      }}
                      className="flex gap-3 px-4 py-3 transition-colors hover:bg-accent"
                    >
                      <div
                        className={cn(
                          "grid size-8 shrink-0 place-items-center rounded-md",
                          meta.cls,
                        )}
                      >
                        <meta.icon className="size-4" />
                      </div>
                      <div className="min-w-0 flex-1">
                        <div className="flex items-start justify-between gap-2">
                          <p className="text-sm font-medium leading-tight">
                            {n.title}
                          </p>
                          {!n.read ? (
                            <span className="mt-1 size-1.5 shrink-0 rounded-full bg-primary" />
                          ) : null}
                        </div>
                        <p className="mt-0.5 line-clamp-2 text-xs text-muted-foreground leading-snug">
                          {n.body}
                        </p>
                        <p className="mt-1 text-[10px] text-muted-foreground">
                          {format(parseISO(n.createdAt), "MMM d · h:mm a")}
                        </p>
                      </div>
                    </Link>
                    {n.inlineAction ? (
                      <div className="flex items-center gap-1.5 px-4 pb-3">
                        <Button
                          size="sm"
                          className="h-7 px-2 text-[11px]"
                          onClick={handlePrimary(n)}
                        >
                          <Check className="size-3" />
                          {n.inlineAction.primaryLabel}
                        </Button>
                        <Button
                          size="sm"
                          variant="outline"
                          className="h-7 px-2 text-[11px]"
                          onClick={handleSecondary(n)}
                        >
                          <X className="size-3" />
                          {n.inlineAction.secondaryLabel}
                        </Button>
                      </div>
                    ) : null}
                  </motion.div>
                );
              })}
            </AnimatePresence>
          )}
        </div>

        <div className="border-t px-4 py-2">
          <Link
            href={`/${orgSlug}/dashboard`}
            onClick={() => setOpen(false)}
            className="block rounded-md py-1.5 text-center text-xs font-medium text-primary hover:bg-primary/5"
          >
            View all activity
          </Link>
        </div>
      </DropdownMenuContent>
    </DropdownMenu>
  );
}
